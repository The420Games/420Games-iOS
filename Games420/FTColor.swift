import Foundation

extension UIColor {
    
    class func ftFacebookBlue() -> UIColor {
        return UIColor(red: 59/255.0, green: 89/255.0, blue: 152/255.0, alpha: 1.0)
    }
    
    class func ftLimeGreen() -> UIColor {
        return UIColor(red: 141/255.0, green: 198/255.0, blue: 63/255.0, alpha: 1.0)
    }
    
    class func ftMainBackgroundColor() -> UIColor {
        return UIColor.blackColor()
    }
    
    class func ftMidGray() -> UIColor {
        return UIColor(red: 153/255.0, green: 153/255.0, blue: 153/255.0, alpha: 1.0)
    }
    
    class func ftMidGray2() -> UIColor {
        return UIColor(red: 161/255.0, green: 161/255.0, blue: 161/255.0, alpha: 1.0)
    }
    
    class func ftAlmostBlack() -> UIColor {
        return UIColor(red: 21/255.0, green: 21/255.0, blue: 21/255.0, alpha: 1.0)
    }
    
    class func ftStravaOrange() -> UIColor {
        return UIColor(red: 252/255.0, green: 76/255.0, blue: 2/255.0, alpha: 1.0)
    }
}
